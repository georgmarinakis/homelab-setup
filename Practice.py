# Tuple
# ordered, unchangeable and allow duplicate values
thistuple = ("apple", "banana", "cherry")
print("I am a Tuple")
print(thistuple)

# List
# ordered, changeable and allow duplicate values
thislist = ["apple", "banana", "cherry"]
print("I am a List")
print(thislist)

# Set
# unordered, unchangeable and doesnt allow duplicate values
thisset = {"apple", "banana", "cherry"}
print("I am a Set")
print(thisset)

# Dictionary
# ordered*, changeable and doesnt allow duplicate values
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
print("I am a Dictionary")
print(thisdict)
print("\n")